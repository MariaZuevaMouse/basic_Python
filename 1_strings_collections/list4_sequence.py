friend_name = 'Max'
friends = ['Max', 'Leo', 'Kate']
roles = ('admin', 'guest', 'user')

# data type
print(type(friend_name))
print(type(friends))
print(type(roles))

#  indexing
print(friend_name[1])
print(friends[1])
print(roles[1])

# substring / sublist  - срезы
print(friend_name[1:])
print(friends[1:])
print(roles[1:])

# length
print(len(friend_name))
print(len(friends))
print(len(roles))
