# full str
print('Hello, I am Mari')
# print with vars
name = 'Mari'
age = 16
# space as separator
print(name, age)

# use separator
print(name, age, sep='/')
print(name, age, sep='/;n')

# use end, default -new line
print(name)
print(age)

print('--------------->')
print(name, end=';')
print(age, end=';')
print('end', end=';')
